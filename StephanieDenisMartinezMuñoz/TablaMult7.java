class TablaMult7 {        //Tabla de multiplicacion de 7
    public static void main(String[] args) {        //metodo principal
        int a = 7;                                    //numero de iteraciones   
        for (int i = 0; i <= 10; i++){                           //bucle for
            System.out.println(a * i);                          //impresion
        }                                                       //fin bucle for
    }                                                                           //fin metodo principal
}                                                                                   //fin clase