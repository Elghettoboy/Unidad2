class NumerosPares {        //numeros pares
    public static void main(String[] args) {            //metodo principal
        int a = 100;                                    //numero de iteraciones
        for (int i = 0; i <= a; i++){                   //bucle for
            if (i % 2 == 0){                            //condicion 
                System.out.println(i);                  //impresion
            }                                           //fin condicion
        }                                               //fin bucle for

    }                                                       //fin metodo principal
}                                                           //fin clase