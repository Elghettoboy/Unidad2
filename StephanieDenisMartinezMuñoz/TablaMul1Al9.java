class TablaMul1Al9 { //Tabla de multiplicacion de 1 
    public static void main(String[] args) {        //metodo principal
        for (int i = 1; i <= 9; i++){                                //bucle for
            System.out.println("Tabla de multiplicacion de " + i);      //impresion
            for (int j = 0; j <= 10; j++){                            //bucle for
                System.out.println(i + "*" + j + " = " + i * j);       //impresion
            }                                                       //fin bucle for
        }                                                           //fin bucle for
    }                                                               //fin metodo principal
}                                                                   //fin clase 