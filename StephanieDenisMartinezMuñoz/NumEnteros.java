class NumEnteros {  //numeros pares
    public static void main(String[] args) {        //metodo principal
        int a = 2;                                  //inicializacion
        while (a <= 1000){                          //condicion
            System.out.println(a);                      //impresion
            a+=2;                                     //incremento  
        }                                            //fin condicion
    }                                                //fin metodo principal
}                                                    //fin clase
