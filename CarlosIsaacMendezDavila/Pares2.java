class Pares2 {
    public static void main(String[] args) { // metodo principal
        // ciclo for, que itera solo numeros pares
        for (int i = 0; i <= 100; i++) {
            // condicion que si se cumple obtendremos solo numeros pares
            if (i%2==0){
                // impresion de los numeros
                System.out.println("Numero Par: " + i);
            }
        }
    }
}
