// Nombre de la clase
class Siete {
    public static void main(String[] args) { // metodo principal
        // ciclo for, que itera numeros de uno en uno
        for (int i = 1; i <= 10; i ++) {
            /*declaracion de una variable, que almacena la operacion de
             los numeros iterados por 7*/
            int r= i*7;
            // impresion de los numeros
            System.out.println(+i+ " * " +7+ " = " +r);
        }
    }
}